package org.uptick.market.dto;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import lombok.Getter;
import lombok.Setter;

import java.io.Serializable;

@Getter
@Setter
@ApiModel
public class PageFrame implements Serializable {

    public static final Long MAX_PAGE_SIZE = 10000000L;
    @ApiModelProperty(required = true, value = "Page number", example = "1")
    private int pageNumber = 1;

    @ApiModelProperty(required = true, value = "Page size", example = "20")
    private int pageSize = 20;


    public PageFrame() {
    }

    public PageFrame(int pageNumber, int pageSize) {
        this.pageNumber = pageNumber;
        this.pageSize = pageSize;
    }

}
